<h6>PELAPORAN PELANGGARAN</h6>
Individu PTPN VI memiliki kewajiban moral untuk melaporkan terjadinya pelanggaran apabila mengetahuinya. Kesadaran perlunya menyampaikan adanya pelanggaran demi kepentingan dan kemaslahatan bersama serta manfaat untuk mencegah dampak yang tidak diinginkan menyebar luas, seperti misalnya kebiasaan penerimaan atau pemberian gratifikasi.<ol class="c">
  <li><strong>Prinsip Dasar</strong></li>
      <ol class="d">
        <li>Perusahaan wajib menerima pelaporan pelanggaran dari pelapor.</li>
        <li>Pelaporan pelanggaran dari pelapor harus ditempatkan dalam kerangka peningkatan <i>Good Corporate Governance</i>.</li>
        <li>Pelaporan pelanggaran harus dilakukan itikad baik dan bukan merupakan suatu keluhan pribadi atas suatu kebijakan perusahaan tertentu ataupun didasari kehendak buruk/bersifat fitnah/laporan mencemarkan nama baik atau reputasi seseorang.</li>
      </ol>
      <br/>

  <li><strong>Pelapor Pelanggaran (<i>Whistleblower</i>)</strong></li>
       <ol class="d">
          <li>Pada dasarnya pelapor pelanggaran (<i>whistleblower</i>) adalah pihak internal (Direksi, Karyawan, Dewan Komisaris dan organ pendukung Dewan Komisaris), akan tetapi tidak tertutup adanya pelapor berasal dari pihak eksternal (pelanggan, pemasok dan lain-lain).</li>
          <li>Pelapor adalah orang yang melaporkan adanya tindak pelanggaran, tetapi mungkin ia tidak melihat dan mendengar sendiri pelaksanaan tindak pelanggaran tersebut, tetapi mempunyai bukti-bukti surat atau alat bukti petunjuk (rekaman, gambar dan lain sebagainya) bahwa telah terjadi tindak pelanggaran.</li>
          <li>Pelapor Pelanggaran (<i>whistleblower</i>) dapat bertindak menjadi saksi apabila ia melihat dan mendengar atau mengalami sendiri tindak pelanggaran yang dilakukan Terlapor.</li>
          <li>Pelapor harus memberikan bukti, informasi atau indikasi yang jelas atas terjadinya pelanggaran yang dilaporkan, sehingga dapat ditelusuri atau ditindaklanjuti. Untuk mempercepat dan mempermudah proses penanganan pelaporan pelanggaran, maka Pelapor :</li>
              <ol class="b">
                  <li>Pelapor dalam menyampaikan Pelaporan Pelanggaran (whistleblowing) harus mencantumkan identitas mengenai data diri yang memuat alamat rumah/kantor, alamat e-mail, faksimili, nomor kontak dan dapat dihubungi;</li>
                  <li>Harus disertai dengan bukti pendukung atas laporan pelanggaran yang disampaikan, meliputi:</li>
                      <ol class="d">
                          <li>Pokok masalah yang diadukan;</li>
                          <li>Pihak-pihak yang terlibat yaitu siapa saja yang terlibat dalam pelanggaran yang diadukan termasuk pihak-pihak yang dirugikan/diuntungkan dari kasus yang terjadi;</li>
                          <li>Waktu dan Lokasi kejadian yaitu kapan kasus pelanggaran terjadi dan di unit atau fungsi mana di Perusahaan kasus pelanggaran yang diadukan terjadi.</li>
                          <li>Kronologis kasus;</li>
                          <li>Dokumen pendukung atas kasus yang diadukan.</li>
                      </ol>
              </ol>
      </ol>
  <li><strong>Bentuk Pelaporan</strong></li>
      <ol class="d">
        <li>Pelaporan Pelanggaran secara tertulis dan beridentitas, wajib dilengkapi dengan fotokopi identitas dan bukti  pendukung seperti: dokumen yang memuat indikasi awal yang dapat dipertanggungjawabkan dan memberikan petunjuk mengenai transaksi yang dilakukan dan/atau Pelaporan Pelanggaran yang akan disampaikan sebagai bahan pemeriksaan lebih lanjut.</li>
        <li>Pelaporan pelanggaran secara lisan beridentitas, wajib dilengkapi penjelasan/kronologis dugaan pelanggaran yang memuat indikasi awal yang dapat dipertanggungjawabkan dan memberikan petunjuk mengenai transaksi yang dilakukan dan/atau Pelaporan Pelanggaran yang akan disampaikan sebagai bahan pemeriksaan lebih lanjut serta bukti pendukung lainnya.</li>
        <li>Apabila Pelaporan Pelanggaran diajukan oleh perwakilan Pelapor, maka selain dokumen di atas juga diserahkan dokumen lainnya yaitu:</li>
              <ol class="a">
                  <li>Fotokopi bukti identitas Pelapor dan perwakilan Pelapor.</li>
                  <li>Surat Kuasa dari Pelapor kepada perwakilan Pelapor yang menyatakan bahwa Pelapor  memberikan kewenangan bertindak untuk dan atas nama Pelapor. Jika perwakilan Pelapor adalah lembaga atau badan hukum, maka harus dilampiri dengan dokumen yang  menyatakan bahwa pihak yang mengajukan pengaduan berwenang untuk mewakili lembaga atau badan hukum tersebut.</li>
              </ol>
      </ol>
      <br/>
  <li><strong>Pelanggaran Yang Dapat Dilaporkan</strong></li>
      Pelanggaran dapat dilaporkan adalah perbuatan yang dalam pandangan pelapor dengan itikad baik adalah perbuatan sebagai berikut:
      <ol class="d">
          <li>Korupsi</li>
          <li>Kecurangan</li>
          <li>Ketidakjujuran</li>
          <li>Perbuatan melanggar hukum (termasuk pencurian, penggunaan kekerasan terhadap karyawan atau pimpinan, pemerasan, penggunaan narkoba, pelecehan, perbuatan kriminal lainnya)</li>
          <li>Pelanggaran ketentuan perpajakan, dan/atau peraturan perundang-undangan lainnya</li>
          <li>Pelanggaran Pedoman Perilaku Perusahaan atau pelanggaran norma-norma kesopanan pada umumnya</li>
          <li>Perbuatan yang membahayakan keselamatan dan kesehatan kerja, atau membahayakan keamanan perusahaan</li>
          <li>Perbuatan yang dapat menimbulkan kerugian finansial atau non-finansial terhadap perusahaan atau merugikan kepentingan perusahaan</li>
          <li>Pelanggaran Prosedur Standar Operasional (SOP) perusahaan, terutama terkait dengan pengadaan barang dan jasa, pemberian manfaat dan remunerasi</li>
      </ol>
      <br/>
  <li><strong>Waktu Untuk Melaporkan Pelanggaran</strong></li>
  Pelapor harus mempunyai alasan yang kuat dalam menyampaikan laporan pelanggaran ataupun potensi pelanggaran. Pelaporan  seyogyanya dilakukan segera dan dalam waktu tidak lebih dari 3 (tiga) bulan, setelah terjadinya indikasi pelanggaran. Hal ini bertujuan untuk lebih mempermudah proses tindak lanjut. Selama rentang waktu pelaporan, laporan pelanggaran dapat tidak ditindaklanjuti jika hal tersebut sudah terlanjur dikoreksi sehingga tidak diketemukan bukti lagi.
</ol>

