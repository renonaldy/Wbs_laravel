<h6>STRUKTUR PENGELOLAAN WBS</h6>
Dalam penyusunan infrastruktur WBS, hal yang terpenting adalah kejelasan dari akuntabilitas dan tanggung jawab untuk mendorong pelaksanaan WBS di perusahaan. <p class="text-sm">
<ol class="c">
  <li><strong>Direksi</strong></li>
      Direksi bertanggungjawab atas efektivitas rancangan, pelaksanaan dan pemeliharaan penyelenggaraan WBS secara keseluruhan serta berkewajiban menetapkan arahan dan melakukan tindakan-tindakan untuk menjamin bahwa seluruh aktivitas penyelenggaraan WBS berjalan dengan baik.
      <br/>
      Direksi berwenang untuk:
      <ol class="d">
        <li>Membentuk dan menetapkan Administrator dan Komite Pengelola WBS.</li>
        <li>Memutuskan untuk dihentikan atau dilanjutkannya sebuah pelaporan pelanggaran.</li>
        <li>Menugaskan tim investigasi untuk melakukan  investigasi  jika  ditemukan  indikasi  awal  yang  kuat  pada  saat dilaksanakan klarifikasi awal.</li>
        <li>Memberikan sanksi kepada terlapor sesuai dengan ketentuan yang berlaku di perusahaan  atau meneruskan  kepada pihak yang berwajib jika terbukti bersalah atas pelanggaran yang dilakukannya.</li>
        <li>Memberikan apresiasi kepada Pelapor jika pelaporan terbukti sesuai dengan ketentuan yang berlaku di perusahaan.</li>
      </ol>
      <br/>
  <li><strong>Dewan Komisaris</strong></li>
      Dewan Komisaris bertanggung jawab melakukan pengawasan atas kecukupan dan efektivitas pelaksanaaan WBS diperusahaan. Pemantauan pelaksanaan WBS dapat diserahkan kepada Komite Dewan Komisaris.  <br/><br/>
  <li><strong>Komite Pengelola WBS</strong></li>
        Komite Pengelola WBS merupakan fungsi atau unit yang dibentuk dan ditetapkan Direksi untuk menyelenggarakan dan mengelola jalur komunikasi bagi Pelapor untuk melaporkan indikasi awal, melakukan klarifikasi awal dan melakukan investigasi atas pelaporan pelanggaran. Komite Pengelola WBS harus independen dari operasi perusahaan sehari-hari dan  mempunyai akses kepada pimpinan tertinggi perusahaan.
       <br/>
       Unsur dari Komite Pengelola WBS terdiri dari 2 (dua) elemen utama yaitu:
      <ol class="d">
          <li>Administrator WBS yaitu sub-unit yang bertugas :</li>
              <ol class="a">
                  <li>Mengumpulkan seluruh laporan pelanggaran.</li>
                  <li>Mengkompilasi seluruh laporan pelanggaran.</li>
                  <li>Menyerahkan hasil kompilasi seluruh laporan pelanggaran kepada Komite Pengelola WBS.</li>
              </ol>
          <li>Komite Pengelola WBS yaitu sub-unit yang bertugas :</li>
              <ol class="a">
                  <li>Melakukan penelaahan seluruh pelaporan pelanggaran yang dikompilasi oleh Administrator WBS.</li>
                  <li>Memutuskan tindak lanjut terhadap laporan pelanggaran.</li>
                  <li>Melakukan reviu keterkaitan antara pelanggaran yang dilaporkan dengan ketentuan yang berlaku di Perusahaan.</li>
                  <li>Membuat laporan atas hasil tindak lanjut laporan pelanggaran secara tertulis kepada Direksi.</li>
                  <li>Membuat laporan triwulanan kepada Direksi terkait pelaksanaan tugas Komite Pengelola WBS.</li>
              </ol>
      </ol>
</ol>

